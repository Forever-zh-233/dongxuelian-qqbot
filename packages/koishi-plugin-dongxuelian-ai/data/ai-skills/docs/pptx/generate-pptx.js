#!/usr/bin/env node
/**
 * Controlled PPTX generator for Agent docs skill.
 * Usage: node generate-pptx.js slides.json output.pptx
 */
const fs = require('fs')
const path = require('path')

function fail(message) {
  console.error(message)
  process.exit(1)
}

function safeOutputPath(file) {
  const root = path.resolve(process.env.DONGXUELIAN_AI_DATA_DIR || path.join(__dirname, '..', '..', '..'), 'agent-docs')
  const target = path.resolve(file || path.join(root, `agent-${Date.now()}.pptx`))
  const rel = path.relative(root, target)
  if (rel.startsWith('..') || path.isAbsolute(rel)) fail('输出路径必须位于 data/agent-docs')
  fs.mkdirSync(path.dirname(target), { recursive: true })
  return target
}

function loadSlides(file) {
  const data = JSON.parse(fs.readFileSync(file, 'utf8').replace(/^\uFEFF/, ''))
  const slides = Array.isArray(data.slides) ? data.slides : []
  if (!slides.length) fail('slides 不能为空')
  return {
    title: String(data.title || 'Agent PPT').slice(0, 80),
    slides: slides.slice(0, 80).map((slide, index) => ({
      title: String(slide.title || `第 ${index + 1} 页`).slice(0, 80),
      bullets: (Array.isArray(slide.bullets) ? slide.bullets : String(slide.content || '').split(/\r?\n/))
        .map(item => String(item || '').trim()).filter(Boolean).slice(0, 8),
      notes: String(slide.notes || '').slice(0, 1000),
    })),
  }
}

async function main() {
  const input = process.argv[2]
  if (!input) fail('缺少 slides.json')
  let pptxgen
  try {
    pptxgen = require('pptxgenjs')
  } catch {
    fail('缺少 pptxgenjs 依赖。请先安装：npm install pptxgenjs')
  }
  const output = safeOutputPath(process.argv[3])
  const data = loadSlides(input)
  const pptx = new pptxgen()
  pptx.layout = 'LAYOUT_WIDE'
  pptx.author = 'LianLian Agent'
  pptx.subject = data.title
  pptx.title = data.title
  pptx.company = 'LianLian Bot'
  pptx.lang = 'zh-CN'
  pptx.theme = {
    headFontFace: 'Microsoft YaHei',
    bodyFontFace: 'Microsoft YaHei',
    lang: 'zh-CN',
  }

  const cover = pptx.addSlide()
  cover.background = { color: 'FFF7ED' }
  cover.addText(data.title, { x: 0.8, y: 1.4, w: 11.8, h: 0.8, fontFace: 'Microsoft YaHei', fontSize: 34, bold: true, color: '1F2937' })
  cover.addText('Generated by LianLian Agent', { x: 0.85, y: 2.35, w: 8, h: 0.3, fontSize: 14, color: 'C2410C' })

  for (const item of data.slides) {
    const slide = pptx.addSlide()
    slide.background = { color: 'FFFFFF' }
    slide.addShape(pptx.ShapeType.rect, { x: 0, y: 0, w: 13.33, h: 0.18, fill: { color: 'F97316' }, line: { color: 'F97316' } })
    slide.addText(item.title, { x: 0.65, y: 0.55, w: 12, h: 0.55, fontSize: 25, bold: true, color: '111827' })
    const bulletText = item.bullets.map(text => ({ text, options: { bullet: { type: 'ul' }, breakLine: true } }))
    slide.addText(bulletText.length ? bulletText : [{ text: '（暂无要点）', options: {} }], { x: 0.9, y: 1.45, w: 11.7, h: 4.6, fontSize: 18, color: '374151', breakLine: false, fit: 'shrink' })
    if (item.notes) slide.addNotes(item.notes)
  }

  await pptx.writeFile({ fileName: output })
  console.log(output)
}

main().catch(error => fail(error.message || String(error)))
